// An array is a data structure that stores multiple values in a single variable.
let marks = [97, 82, 75, 64, 36];  

// console.log() is used to print output to the console.
console.log(marks);  

// .length is a property that returns the total number of elements in the array.
console.log(marks.length);  // property

// Another example of an array storing string values (names of heroes)
let heroes = ["ironman", "thor", "spiderman", "superman", "antman", "daredevils"];

// Prints the heroes array to the console
console.log(heroes);

// Prints the number of elements in the heroes array
console.log(heroes.length);  // property
